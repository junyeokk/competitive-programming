n, m = map(int, input().split())

arr = []
shapes = [
    # I
    [(0, 0), (0, 1), (0, 2), (0, 3)],
    [(0, 0), (1, 0), (2, 0), (3, 0)],

    # T
    [(0, 0), (1, 0), (2, 0), (1, 1)],
    [(0, 0), (0, 1), (0, 2), (-1, 1)],
    [(0, 0), (0, 1), (0, 2), (1, 1)],
    [(0, 0), (1, 0), (2, 0), (1, -1)],

    # S
    [(0, 0), (0, 1), (1, 1), (1, 2)],
    [(1, 0), (2, 0), (1, 1), (0, 1)],
    [(0, 0), (1, 0), (1, 1), (2, 1)],
    [(0, 1), (0, 2), (1, 0), (1, 1)],

    # L
    [(0, 0), (0, 1), (0, 2), (1, 2)],
    [(2, 0), (0, 1), (1, 1), (2, 1)],
    [(0, 0), (1, 0), (1, 1), (1, 2)],
    [(0, 0), (1, 0), (2, 0), (0, 1)],

    # J
    [(0, 2), (1, 0), (1, 1), (1, 2)],
    [(0, 0), (1, 0), (2, 0), (2, 1)],
    [(0, 0), (0, 1), (0, 2), (1, 0)],
    [(0, 0), (0, 1), (1, 1), (2, 1)],

    # O
    [(0, 0), (0, 1), (1, 0), (1, 1)], 
]

for i in range(n):
    s = list(map(int, input().split()))
    arr.append(s)

res = 0
for i in range(n):
    for j in range(m):
        for shape in shapes:
            tsum = 0
            valid = True
            for dr, dc in shape:
                nr = i + dr
                nc = j + dc
                if 0 <= nr < n and 0 <= nc < m:
                    tsum += arr[nr][nc]
                else:
                    valid = False
                    break
            if valid:
                res = max(res, tsum)

print(res)